      SUBROUTINE FICHR(LU,INAM,STA,IOF)
      CHARACTER INAM*6,STA*3
      GOTO (10,20)IOF
 10   IF(STA.EQ.'NEW')THEN
      OPEN(LU,FILE=INAM,ACCESS='SEQUENTIAL',STATUS='NEW')
      ELSE
      OPEN(LU,FILE=INAM,ACCESS='SEQUENTIAL',STATUS='OLD')
      ENDIF
      RETURN
 20   CLOSE(LU,STATUS='KEEP')
      RETURN
      END
