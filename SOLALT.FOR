      PROGRAM SOLALT
C     CALCULATES THE SOLAR ALTITUDE AT A GIVEN PLACE, FOR A GIVEN TIME
C
      PI=4.*ATAN(1.)
      R=PI/180.
      WRITE(*,9000)
 9000 FORMAT(1X,' ENTER',/, 
     &'          AZIMUTH OF THE WINDOW (NORTH=0, CLOCKWISE)',/,
     &'          LATITUDE OF THE PLACE',/,
     &'          DAY,MONTH,HOUR OF MAX. SHADING')
      READ(*,*)WAZ,SLAT,D,M,HR
      CALL SDEC(D,M,DEC)
      CALL SANG(HR,D,M,SLAT,DEC,SALT,SAZM)
      WRITE(*,*)SALT/R,SAZM/R
      END      