C ******************************************************************
C
C PROGRAM TRANSDAT.FOR
C PROGRAM TO MODIFY THE TIME IN THE GRANT SQUIRREL DATA FILES
C USED IN THE PASCOOL PROJECT
C
C ATHANASSIOS A. ARGIRIOU, University of Athens
C 27-9-1993
C**********************************************************************
      PROGRAM TRANSDAT
C
      CHARACTER MONTH*3,BIDON*155
      DIMENSION X(50)  
C
C
      INLIN=24
      NCOL=13
      STEP=15./1440.
      TIME=270.5417
C
C OPEN DATA FILE AND OUTPUT FILE
      OPEN(100,FILE='GR933020')
      OPEN(200,FILE='TRANSOUT')
C READ THE COMMENT LINES UNTIL BEGINNING OF DATA
      DO I=1,INLIN
      READ(100,'(A)',ERR=910)BIDON
      WRITE(200,'(A)')BIDON
      ENDDO
C    
 30   READ(100,1200,ERR=910)IDAY,MONTH,IYEAR,IHOUR,MIN,ISEC,(X(J),
     *J=1,NCOL)
 1200 FORMAT(1X,I2,1X,A3,1X,I2,I2,1X,I2,1X,I2,13F9.2) 
      TIME=TIME+STEP
      WRITE(200,1300)TIME,(X(J),J=1,NCOL) 
 1300 FORMAT(F8.4,13F9.2)
      GOTO 30 
C
  910 CLOSE(100)      
      CLOSE(200)
      END
